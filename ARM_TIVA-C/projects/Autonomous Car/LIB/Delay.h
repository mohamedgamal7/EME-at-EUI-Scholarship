#ifndef DELAY_H__
#define DELAY_H__

/********************************************************************************************/
/* Function: delay_ms                                                                    */
/* Description: Delays the program execution for a specified duration in milliseconds (ms).  */
/*              This function introduces a delay in the program, causing it to pause for     */
/*              the specified time before continuing execution.                              */
/* Arguments:                                                                              */
/*   - m: The duration of the delay in milliseconds.                                        */
/********************************************************************************************/
void delay_ms(int m);

/********************************************************************************************/
/* Function: delay_us                                                                    */
/* Description: Delays the program execution for a specified duration in microseconds (us).  */
/*              This function introduces a delay in the program, causing it to pause for     */
/*              the specified time before continuing execution.                              */
/* Arguments:                                                                              */
/*   - m: The duration of the delay in microseconds.                                       */
/********************************************************************************************/
void delay_us(int m);

#endif
